CREATE PROCEDURE SP_ReportePagos
AS
SET NOCOUNT ON
SET TRANSACTION ISOLATION LEVEL READ COMMITTED
BEGIN TRAN
SELECT	REGISTRAR_PAGO.ID_PAGO,
		CLIENTE.NOMBRE_COMPLETO AS CLIENTE,
		REGISTRAR_PAGO.ID_CONTRATO AS CONTRATO,
		CONTRATO.ESTADO AS ESTADO_CONTRATO,
		REGISTRAR_PAGO.TIPO_PAGO,
		REGISTRAR_PAGO.MONTO
FROM REGISTRAR_PAGO
INNER JOIN CLIENTE ON CLIENTE.ID_CLIENTE = REGISTRAR_PAGO.ID_CLIENTE
INNER JOIN CONTRATO ON CONTRATO.ID_CONTRATO = REGISTRAR_PAGO.ID_CONTRATO
COMMIT

EXEC SP_ReportePagos
