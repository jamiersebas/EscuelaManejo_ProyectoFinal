alter PROCEDURE SP_ReporteContrato
AS
SET NOCOUNT ON
SET TRANSACTION ISOLATION LEVEL READ COMMITTED
BEGIN TRAN
SELECT	CONTRATO.ID_CONTRATO,
		CONTRATO.ESTADO ,
		CLIENTE.NOMBRE_COMPLETO AS CLIENTE,
		VEHICULO.MARCA AS VEHICULO,
		--PERSONAL.NOMBRE_PERSONAL AS PERSONAL,
		CONTRATO.FECHA_INICIO AS FECHA
FROM CONTRATO
INNER JOIN CLIENTE ON  CONTRATO.ID_CLIENTE = CLIENTE.ID_CLIENTE
INNER JOIN VEHICULO ON CONTRATO.ID_PLACA = VEHICULO.ID_PLACA
INNER JOIN PERSONAL ON  CONTRATO.ID_PERSONAL = PERSONAL.ID_PERSONAL
COMMIT


EXEC SP_ReporteContrato

