CREATE TABLE CATALOGO_DIRECCION(
ID_CATALOGO INT PRIMARY KEY,
NUM_CASA VARCHAR(50),
CALLE_AVENIDA VARCHAR(50),
CIUDAD VARCHAR(50),
COLONIA VARCHAR(50),
CODIGO_POSTAL INT,
DEPARTAMENTO VARCHAR(50),
MUNICIPIO VARCHAR(50),
ESTADO VARCHAR(50),
)
GO

CREATE TABLE PERSONAL(
ID_PERSONAL INT PRIMARY KEY,
DIRECCION VARCHAR(50),
NOMBRE_PERSONAL VARCHAR(50),
APELLIDO_PERSONAL VARCHAR (50),
FECHA_NACIMIENTO VARCHAR(50),
FECHA_CONTRATACION VARCHAR(50),
TELEFONO_PERSONAL INT,
EMAIL_PERSONAL VARCHAR(50),
FECHA_BAJA VARCHAR(50),
ESTADO VARCHAR(50),
PUESTO VARCHAR(50),
ID_CATALOGO INT
)
GO

CREATE TABLE CONTRATO(
ID_CONTRATO INT PRIMARY KEY,
SESION INT,
ID_PERSONAL INT,
ID_PLACA INT,
FECHA_INICIO VARCHAR(50),
FECHA_FIN VARCHAR(50),
COSTO INT,
ESTADO VARCHAR(50),
ID_CLIENTE INT
)
GO

CREATE TABLE VEHICULO(
ID_PLACA INT PRIMARY KEY,
MARCA VARCHAR(50),
MODELO VARCHAR(50),
AÑO VARCHAR(5),
FECHA_COMPRA VARCHAR(50)
)
GO

CREATE TABLE CLASE(
ID_CLASE INT PRIMARY KEY,
ID_CLIENTE INT,
ID_CONTRATO INT,
HORA_INICIO VARCHAR(50),
HORA_FIN VARCHAR(50),
FECHA VARCHAR(50)
)
GO

CREATE TABLE CLIENTE(
ID_CLIENTE INT PRIMARY KEY,
NOMBRE_COMPLETO VARCHAR(50),
DIRECCION VARCHAR(50),
FECHA_NACIMIENTO VARCHAR(50),
TELEFONO INT,
EMAIL VARCHAR (50),
ESTADO VARCHAR(50)
)
GO 

CREATE TABLE REGISTRAR_PAGO(
ID_PAGO INT PRIMARY KEY,
FECHA_PAGO VARCHAR(50),
TIPO_PAGO VARCHAR(50),
MONTO INT,
ID_CLIENTE INT,
ID_CONTRATO INT
)
GO


